export const LOCALES = {
  'en-GB': EN_GB, 'en-US': EN_US, 'en-NG': EN_NG,
  'es-ES': ES_ES, 'es-419': ES_419, 'pt-BR': PT_BR,
  'fr-FR': FR_FR, 'de-DE': DE_DE, ar: AR,
  sw: SW, zu: ZU, hi: HI, 'zh-Hans': ZH, ja: JA,
};

// fold the extra translations into their locales
for (const [code, patch] of Object.entries(EXTRA)) {
  if (LOCALES[code]) LOCALES[code] = merge(LOCALES[code], patch);
}

/** Deep-merge a locale over the English base so partial translations still work. */
function merge(base, over) {
  if (Array.isArray(over)) return over;
  if (over && typeof over === 'object' && !Array.isArray(base) && typeof base === 'object') {
    const out = { ...base };
    for (const k of Object.keys(over)) out[k] = merge(base[k], over[k]);
    return out;
  }
  return over === undefined ? base : over;
}

export function getLocale(code) {
  const pack = LOCALES[code] || LOCALES['en-GB'];
  // walk the inheritance chain: dialects merge over their parent language first
  const chain = [];
  let cur = pack;
  const guard = new Set();
  while (cur) {
    chain.unshift(cur);
    const parent = cur.base && !guard.has(cur.base) ? LOCALES[cur.base] : null;
    if (cur.base) guard.add(cur.base);
    cur = parent;
  }
  let merged = EN_GB;
  for (const link of chain) merged = merge(merged, link);
  merged.code = LOCALES[code] ? code : 'en-GB';
  merged.name = pack.name;
  merged.dir = pack.dir || 'ltr';
  return merged;
}

/** Best guess from the phone's own settings, before the user chooses. */
export function detectLocale(langs = (typeof navigator !== 'undefined' ? navigator.languages : []) || []) {
  const codes = Object.keys(LOCALES);
  // Respect the phone's own priority order: resolve each preference fully
  // (exact match, then language-family match) before moving to the next one.
  for (const raw of langs) {
    const l = String(raw).toLowerCase();
    const exact = codes.find((c) => c.toLowerCase() === l);
    if (exact) return exact;

    const short = l.split('-')[0];
    if (short === 'es') return l === 'es-es' ? 'es-ES' : 'es-419';
    if (short === 'pt') return 'pt-BR';
    if (short === 'en') return l === 'en-us' ? 'en-US' : 'en-GB';
    if (short === 'zh') return 'zh-Hans';
    const family = codes.find((c) => c.split('-')[0] === short);
    if (family) return family;
  }
  return 'en-GB';
}

/** Fill {placeholders} in a string. */
export const fill = (str, vars = {}) =>
  String(str ?? '').replace(/\{(\w+)\}/g, (m, k) => (k in vars ? vars[k] : m));
